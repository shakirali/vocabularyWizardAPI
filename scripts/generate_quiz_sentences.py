def create_blank_sentence(sentence: str, word: str) -> str:
    """Convert a sentence with the word into a fill-in-the-blank format."""
    patterns = [
        word,
        word + "s",
        word + "ed",
        word + "ing",
        word + "ly",
        word + "ness",
        word + "ful",
        word + "less",
    ]
    
    if word.endswith('e'):
        patterns.extend([
            word[:-1] + "ed",
            word[:-1] + "ing",
        ])
    
    if len(word) > 2 and word[-1] not in 'aeiou' and word[-2] in 'aeiou':
        patterns.extend([
            word + word[-1] + "ed",
            word + word[-1] + "ing",
        ])
    
    if word.endswith('y'):
        patterns.extend([
            word[:-1] + "ied",
            word[:-1] + "ies",
            word[:-1] + "ier",
            word[:-1] + "iest",
        ])
    
    result = sentence
    for pattern in patterns:
        regex = re.compile(re.escape(pattern), re.IGNORECASE)
        result = regex.sub("_____", result)
    
    return result


