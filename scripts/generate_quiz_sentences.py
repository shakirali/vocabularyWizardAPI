def generate_sentences_for_word(word: str, example_sentence: str, count: int = 10) -> List[str]:
    """Generate quiz sentences for a word."""
    sentences = []
    word_lower = word.lower()
    
    if example_sentence:
        blank_sentence = create_blank_sentence(example_sentence, word)
        if "___" in blank_sentence:
            sentences.append(blank_sentence)
    
    if word_lower in WORD_SENTENCES:
        for sentence in WORD_SENTENCES[word_lower]:
            blank = create_blank_sentence(sentence, word)
            if "___" in blank and blank not in sentences:
                sentences.append(blank)
            if len(sentences) >= count:
                break
    
    return sentences[:count]


def generate_quiz_sentences_csv(year: str, output_path: Path) -> int:
    """Generate quiz sentences CSV for a specific year."""
    vocab_path = project_root / "data" / f"vocabulary_{year}.csv"
    
    if not vocab_path.exists():
        print(f"Error: Vocabulary file not found: {vocab_path}")
        return 0
    
    words_data = []
    with open(vocab_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            word = row.get("word", "").strip()
            meaning = row.get("meaning", "").strip()
            example = row.get("example_sentences", "").strip()
            if word:
                words_data.append({
                    "word": word,
                    "meaning": meaning,
                    "example_sentence": example
                })
    
    print(f"Found {len(words_data)} words for {year}")
    
    sentences_data = []
    words_with_custom = 0
    words_with_few = 0
    
    for word_info in words_data:
        word = word_info["word"]
        example = word_info["example_sentence"]
        
        word_sentences = generate_sentences_for_word(word, example, 10)
        
        if word.lower() in WORD_SENTENCES:
            words_with_custom += 1
        
        if len(word_sentences) < 10:
            words_with_few += 1
        
        for sentence in word_sentences:
            sentences_data.append({
                "year": year,
                "word": word,
                "sentence": sentence
            })
    
    with open(output_path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["year", "word", "sentence"])
        writer.writeheader()
        writer.writerows(sentences_data)
    
    print(f"Generated {len(sentences_data)} sentences for {year}")
    print(f"  - Words with custom sentences: {words_with_custom}")
    if words_with_few > 0:
        print(f"  - Words needing more sentences: {words_with_few}")
    
    return len(sentences_data)


def main():
    parser = argparse.ArgumentParser(
        description="Generate high-quality quiz sentences from vocabulary"
    )
    parser.add_argument(
        "--year",
        choices=["year3", "year4", "year5", "year6", "all"],
        default="all",
        help="Year to generate sentences for (default: all)"
    )
    
    args = parser.parse_args()
    
    years = ["year3", "year4", "year5", "year6"] if args.year == "all" else [args.year]
    
    total_sentences = 0
    for year in years:
        output_path = project_root / "data" / f"quiz_sentences_{year}.csv"
        count = generate_quiz_sentences_csv(year, output_path)
        total_sentences += count
    
    print(f"\nTotal sentences generated: {total_sentences}")
    
    if total_sentences < 8000:
        print("\n  Note: Some words have fewer than 10 sentences.")
        print("   Add more word-specific sentences to WORD_SENTENCES dictionary")
        print("   for full coverage of all vocabulary words.")


if __name__ == "__main__":
    main()
